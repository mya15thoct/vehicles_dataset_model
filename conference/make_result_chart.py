#!/usr/bin/env python3
"""Generate a compact condition-wise result chart for the conference paper."""

from __future__ import annotations

import argparse
import csv
from pathlib import Path

from PIL import Image, ImageDraw, ImageFont


CONDITION_ORDER = [
    "morning_norain",
    "evening_norain",
    "morning_rain",
    "evening_rain",
]

CONDITION_LABELS = {
    "morning_norain": "Morning\nNo rain",
    "evening_norain": "Evening\nNo rain",
    "morning_rain": "Morning\nRain",
    "evening_rain": "Evening\nRain",
}

INK = (31, 38, 48)
MUTED = (91, 99, 112)
GRID = (222, 226, 232)
BG = (255, 255, 255)
RANK1_COLOR = (50, 116, 184)
MAP_COLOR = (220, 132, 59)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--summary",
        default="results/conference_50_breakdowns/breakdown_summary.csv",
        help="Breakdown summary CSV generated by conference/evaluate_breakdowns.py.",
    )
    parser.add_argument("--model", default="osnet_ain_x1_0")
    parser.add_argument("--output-root", default="docs/figures/result_charts")
    parser.add_argument("--width", type=int, default=1250)
    parser.add_argument("--height", type=int, default=650)
    return parser.parse_args()


def load_font(size: int, bold: bool = False) -> ImageFont.ImageFont:
    candidates = [
        "/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf" if bold else "/usr/share/fonts/truetype/dejavu/DejaVuSans.ttf",
        "C:/Windows/Fonts/arialbd.ttf" if bold else "C:/Windows/Fonts/arial.ttf",
    ]
    for candidate in candidates:
        path = Path(candidate)
        if path.exists():
            return ImageFont.truetype(str(path), size=size)
    return ImageFont.load_default()


def read_condition_rows(path: Path, model: str) -> dict[str, dict]:
    with path.open("r", newline="", encoding="utf-8") as fh:
        rows = list(csv.DictReader(fh))
    result = {}
    for row in rows:
        if row["breakdown"] == "condition" and row["model"] == model:
            result[row["subset"]] = row
    missing = [condition for condition in CONDITION_ORDER if condition not in result]
    if missing:
        raise SystemExit(f"Missing condition rows for {model}: {', '.join(missing)}")
    return result


def text_size(draw: ImageDraw.ImageDraw, text: str, font: ImageFont.ImageFont) -> tuple[int, int]:
    bbox = draw.textbbox((0, 0), text, font=font)
    return bbox[2] - bbox[0], bbox[3] - bbox[1]


def draw_multiline_centered(
    draw: ImageDraw.ImageDraw,
    center_x: int,
    y: int,
    text: str,
    font: ImageFont.ImageFont,
    fill: tuple[int, int, int],
    line_gap: int = 3,
) -> None:
    lines = text.split("\n")
    cursor = y
    for line in lines:
        tw, th = text_size(draw, line, font)
        draw.text((center_x - tw // 2, cursor), line, fill=fill, font=font)
        cursor += th + line_gap


def draw_chart(rows: dict[str, dict], width: int, height: int, model: str) -> Image.Image:
    image = Image.new("RGB", (width, height), BG)
    draw = ImageDraw.Draw(image)

    title_font = load_font(34, bold=True)
    subtitle_font = load_font(18)
    label_font = load_font(17, bold=True)
    tick_font = load_font(15)
    value_font = load_font(15, bold=True)
    legend_font = load_font(17, bold=True)

    margin_left = 96
    margin_right = 48
    margin_top = 118
    margin_bottom = 106
    plot_w = width - margin_left - margin_right
    plot_h = height - margin_top - margin_bottom
    y0 = margin_top + plot_h

    draw.text((46, 34), "Condition-wise Re-ID Performance", fill=INK, font=title_font)
    draw.text(
        (48, 78),
        f"OSNet-AIN on the condition-balanced conference test split",
        fill=MUTED,
        font=subtitle_font,
    )

    legend_y = 40
    draw.rounded_rectangle((width - 350, legend_y, width - 330, legend_y + 20), radius=4, fill=RANK1_COLOR)
    draw.text((width - 322, legend_y - 1), "Rank-1", fill=INK, font=legend_font)
    draw.rounded_rectangle((width - 220, legend_y, width - 200, legend_y + 20), radius=4, fill=MAP_COLOR)
    draw.text((width - 192, legend_y - 1), "mAP", fill=INK, font=legend_font)

    for tick in range(0, 101, 20):
        y = y0 - int(plot_h * tick / 100)
        draw.line((margin_left, y, width - margin_right, y), fill=GRID, width=1)
        label = f"{tick}"
        tw, th = text_size(draw, label, tick_font)
        draw.text((margin_left - tw - 12, y - th // 2), label, fill=MUTED, font=tick_font)
    draw.text((38, margin_top - 28), "Score (%)", fill=MUTED, font=tick_font)

    group_w = plot_w / len(CONDITION_ORDER)
    bar_w = 48
    inner_gap = 14
    max_score = 100.0

    for index, condition in enumerate(CONDITION_ORDER):
        row = rows[condition]
        rank1 = float(row["rank1"]) * 100.0
        map_score = float(row["mAP"]) * 100.0
        center_x = int(margin_left + group_w * index + group_w / 2)
        left_rank = int(center_x - bar_w - inner_gap / 2)
        left_map = int(center_x + inner_gap / 2)

        for left, value, color in [(left_rank, rank1, RANK1_COLOR), (left_map, map_score, MAP_COLOR)]:
            bar_h = int(plot_h * value / max_score)
            top = y0 - bar_h
            draw.rounded_rectangle((left, top, left + bar_w, y0), radius=8, fill=color)
            value_text = f"{value:.1f}"
            tw, th = text_size(draw, value_text, value_font)
            draw.text((left + bar_w // 2 - tw // 2, top - th - 7), value_text, fill=INK, font=value_font)

        draw_multiline_centered(
            draw,
            center_x,
            y0 + 20,
            CONDITION_LABELS[condition],
            label_font,
            INK,
        )

    draw.line((margin_left, y0, width - margin_right, y0), fill=(160, 166, 176), width=2)
    draw.line((margin_left, margin_top, margin_left, y0), fill=(160, 166, 176), width=2)
    return image


def main() -> int:
    args = parse_args()
    output_root = Path(args.output_root)
    output_root.mkdir(parents=True, exist_ok=True)
    rows = read_condition_rows(Path(args.summary), args.model)
    chart = draw_chart(rows, args.width, args.height, args.model)
    output_path = output_root / f"condition_performance_{args.model}.png"
    chart.save(output_path, dpi=(300, 300))
    print(f"Saved {output_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
