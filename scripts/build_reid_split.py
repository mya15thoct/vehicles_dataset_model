#!/usr/bin/env python3
"""Build query/gallery CSV files from exported Re-ID crops."""

from __future__ import annotations

import argparse
import csv
import shutil
import sys
from collections import Counter
from pathlib import Path


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--manifest",
        default="/mnt/ngan/vehicles/reid_crops/manifest.csv",
        help="Manifest CSV generated by export_reid_crops.py.",
    )
    parser.add_argument(
        "--output-root",
        default="/mnt/ngan/vehicles/reid_benchmark",
        help="Output folder for query/gallery CSV files.",
    )
    parser.add_argument(
        "--copy-images",
        action="store_true",
        help="Also copy crops into query/ and gallery/ folders.",
    )
    return parser.parse_args()


def read_manifest(path: Path) -> list[dict]:
    with path.open("r", newline="", encoding="utf-8") as fh:
        return list(csv.DictReader(fh))


def normalize_view(view: str) -> str:
    if view.startswith("before"):
        return "before"
    if view.startswith("after"):
        return "after"
    return view


def write_csv(path: Path, rows: list[dict]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fields = [
        "condition",
        "view",
        "vehicle_id",
        "label",
        "frame_id",
        "frame_name",
        "crop_path",
        "source_image",
    ]
    with path.open("w", newline="", encoding="utf-8") as fh:
        writer = csv.DictWriter(fh, fieldnames=fields)
        writer.writeheader()
        for row in rows:
            writer.writerow({field: row[field] for field in fields})


def copy_split_images(rows: list[dict], output_dir: Path) -> None:
    output_dir.mkdir(parents=True, exist_ok=True)
    for row in rows:
        src = Path(row["crop_path"])
        if not src.exists():
            continue
        condition = row["condition"]
        vehicle_id = int(row["vehicle_id"])
        dst = output_dir / condition / f"id_{vehicle_id:06d}" / src.name
        dst.parent.mkdir(parents=True, exist_ok=True)
        if not dst.exists():
            shutil.copy2(src, dst)


def print_stats(name: str, rows: list[dict]) -> None:
    ids = {row["vehicle_id"] for row in rows}
    by_condition = Counter(row["condition"] for row in rows)
    by_label = Counter(row["label"] for row in rows)
    print(f"{name}: images={len(rows)}, ids={len(ids)}")
    print(f"  by_condition={dict(sorted(by_condition.items()))}")
    print(f"  by_label={dict(sorted(by_label.items()))}")


def main() -> int:
    args = parse_args()
    manifest_path = Path(args.manifest)
    output_root = Path(args.output_root)

    if not manifest_path.exists():
        print(f"Missing manifest: {manifest_path}", file=sys.stderr)
        return 1

    rows = read_manifest(manifest_path)
    for row in rows:
        row["view_group"] = normalize_view(row["view"])

    query = [row for row in rows if row["view_group"] == "after"]
    gallery = [row for row in rows if row["view_group"] == "before"]

    shared_keys = {
        (row["condition"], row["vehicle_id"])
        for row in query
    } & {
        (row["condition"], row["vehicle_id"])
        for row in gallery
    }

    query = [
        row for row in query
        if (row["condition"], row["vehicle_id"]) in shared_keys
    ]
    gallery = [
        row for row in gallery
        if (row["condition"], row["vehicle_id"]) in shared_keys
    ]

    write_csv(output_root / "query.csv", query)
    write_csv(output_root / "gallery.csv", gallery)

    if args.copy_images:
        copy_split_images(query, output_root / "query")
        copy_split_images(gallery, output_root / "gallery")

    print(f"Manifest: {manifest_path}")
    print(f"Output root: {output_root}")
    print(f"Shared condition/id pairs: {len(shared_keys)}")
    print_stats("query", query)
    print_stats("gallery", gallery)
    return 0


if __name__ == "__main__":
    sys.exit(main())
